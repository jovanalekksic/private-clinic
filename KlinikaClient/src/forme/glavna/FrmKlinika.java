    public void popuniCmbLista() {
        try {
            cmbDoktori.removeAllItems();
            cmbPacijenti.removeAllItems();

            ArrayList<Pacijent> pacijenti=KontrolerKlijent.getInstance().vratiPacijente();
            ArrayList<Doktor> doktori= KontrolerKlijent.getInstance().vratiDoktore();
            ArrayList<Usluga> usluge=KontrolerKlijent.getInstance().vratiUsluge();

            DefaultListModel<Usluga> listModel=new DefaultListModel<>();
            listUsluge.setModel(listModel);
            for (Usluga usluga : usluge) {
                listModel.addElement(usluga);
            }

            for (Pacijent pacijent : pacijenti) {
                cmbPacijenti.addItem(pacijent);
            }
            for(Doktor doktor : doktori){
                cmbDoktori.addItem(doktor);
            }
            if(listModel.isEmpty()){
                JOptionPane.showMessageDialog(this, "Sistem ne moze da ucita usluge!");
            }
            if(doktori.isEmpty()){
                JOptionPane.showMessageDialog(this, "Sistem ne moze da ucita doktore!");
            }
            if(pacijenti.isEmpty()){
                JOptionPane.showMessageDialog(this, "Sistem ne moze da ucita pacijente!");
            }
        } catch (Exception e) {
            Logger.getLogger(FrmKlinika.class.getName()).log(Level.SEVERE, null, e);
        }
        
    }

    
