    private void btnDodajStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajStavkuActionPerformed
        // TODO add your handling code here:
        try {
            
            StavkeTableModel ms=(StavkeTableModel) tblStavke.getModel();

           
            pacijent=(Pacijent) cmbPacijenti.getSelectedItem();
            
            Doktor doktor=(Doktor) cmbDoktori.getSelectedItem();

            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy HH:mm:ss");
            String datumVreme = sdf.format(new Date());

            Usluga usluga=(Usluga) listUsluge.getSelectedValue();
            Pregled pregled=new Pregled(0L, pacijent, doktor,usluga, sdf.parse(datumVreme));
            
            
            try {
                KontrolerKlijent.getInstance().sacuvajPregled(pregled);
                JOptionPane.showMessageDialog(this, "Sistem je zapamtio pregled.");
            } catch (Exception exception) {
                JOptionPane.showMessageDialog(this, exception);
            }
            
            
            
            
//            racun=new Racun(0L, 0, pacijent);
//            KontrolerKlijent.getInstance().sacuvajRacun(racun);
//            
//            ArrayList<Racun> sacuvaniRacun=KontrolerKlijent.getInstance().vratiRacun(racun);
//            Racun racunBaza=sacuvaniRacun.get(0);
//            System.out.println(racunBaza.toString());
            
            Stavka stavka=new Stavka(0L, null,  pregled, usluga.getCena());
            
            ms.dodajStavku(stavka);
            ukupanIznos=0;
            
            for(Stavka st:ms.getListaStavki()){
                ukupanIznos+=st.getCena();
            }
            
            txtUkupno.setText(String.valueOf(ukupanIznos));
            
            
            brojac++;
            if(brojac>0){
                cmbPacijenti.setEnabled(false);
            }
            
            TableColumnModel columnModel=tblStavke.getColumnModel();
            columnModel.getColumn(0).setPreferredWidth(10);
            columnModel.getColumn(1).setPreferredWidth(200);
            columnModel.getColumn(2).setPreferredWidth(50);
            
            
        } catch (Exception e) {
            Logger.getLogger(FrmKlinika.class.getName()).log(Level.SEVERE,null,e);
        }
        
        
    }//GEN-LAST:event_btnDodajStavkuActionPerformed

    private void btnSacuvajRacunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajRacunActionPerformed
        // TODO add your handling code here:
        try {
            StavkeTableModel ms=(StavkeTableModel) tblStavke.getModel();
            
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy HH:mm:ss");
            String datumVreme = sdf.format(new Date());
            Racun racun=new Racun(0L, ukupanIznos, pacijent,sdf.parse(datumVreme));
            
            racun.setLista(ms.getListaStavki());
            
            racun.setUkupanIznos(ukupanIznos);
            
            KontrolerKlijent.getInstance().sacuvajRacun(racun);
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio racun.");
        } catch (Exception e) {
            Logger.getLogger(FrmKlinika.class.getName()).log(Level.SEVERE,null,e);
        }
    }//GEN-LAST:event_btnSacuvajRacunActionPerformed

